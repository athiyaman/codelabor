package org.codelabor.javaprogramming.homework.exam7;

public class Rect {
	public int meth(int length) {
		return length * length;
	}
	public int meth(int length1, int length2) {
		return length1 * length2;
	}
	public int meth(int length1, int length2, int length3) {
		return length1 * length2 * length3;
	}
}
